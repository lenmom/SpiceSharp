using System;
using System.Linq;

using SpiceSharp.Attributes;
using SpiceSharp.Components.Mosfets;
using SpiceSharp.Validation;

namespace SpiceSharp.Components
{
    /// <summary>
    /// A Level 3, semi-empirical model (see reference for level 3).
    /// </summary>
    [Pin(0, "Drain"), Pin(1, "Gate"), Pin(2, "Source"), Pin(3, "Bulk"), AutoGeneratedBehaviors]
    [Connected(0, 2), Connected(0, 3)]
    public partial class Mosfet3 : Component<Parameters>,
        IRuleSubject
    {
        /// <summary>
        /// The pin count for mosfets.
        /// </summary>
        [ParameterName("pincount"), ParameterInfo("Number of pins")]
        public const int PinCount = 4;

        /// <summary>
        /// Initializes a new instance of the <see cref="Mosfet3"/> class.
        /// </summary>
        /// <param name="name">The name of the device.</param>
        /// <exception cref="ArgumentNullException">Thrown if <paramref name="name"/> is <c>null</c>.</exception>
        public Mosfet3(string name)
            : base(name, PinCount)
        {
        }

        /// <inheritdoc/>
        void IRuleSubject.Apply(IRules rules)
        {
            ComponentRuleParameters p = rules.GetParameterSet<ComponentRuleParameters>();
            Simulations.IVariable[] nodes = Nodes.Select(name => p.Factory.GetSharedVariable(name)).ToArray();
            foreach (IConductiveRule rule in rules.GetRules<IConductiveRule>())
            {
                rule.AddPath(this, nodes[0], nodes[2], nodes[3]);
                rule.AddPath(this, ConductionTypes.Ac, nodes[0], nodes[1]); // Gate-source capacitance
            }
        }
    }
}
